package 복습;

public class 기본형심화2 {

	public static void main(String[] args) {
//		정수형 변수 4가지
//		byte : -128 ~ 127		(1바이트)
//		short : -3만 ~ 3만		(2바이트)	
//		int : -21억 ~ 21억		(4바이트)
//		long : 21억 이상			(8바이트)		데이터 뒤에 문자 'L'을 써줘야 long 타입으로 이해함

		byte age = 127;
		short days = 30000;
		int money = 10000;
		long back = 2100000000000L;

//		실수형 변수 2가지
//		float : 소수점 4자리			 (4바이트)
//		double : 소수점 4자리 이상 (8바이트)

		float height = 170.4f;
		double weight = 50.025;

//		문자형 1가지
//		char

//		논리형 1가지
//		boolean
	}

}
