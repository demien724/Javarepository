package 조건문;

public class if문1 {
	public static void main(String[] args) {
		int x = 0;
		
		
//		조건을 쓸때는 비교하는것으로 쓰고, 결과는 TRUE OR FALSE
		if (x == 0) {
			System.out.println("내가 실행되겠지요?");
		}else {
			System.out.println("나는 조건이 맞지 않을때 실행됩니다.");
		}
		
		
		if (x==1) {
			System.out.println("나는 실행 될까요?");
		}
	}
}
