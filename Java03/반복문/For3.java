package 반복문;

public class For3 {

	public static void main(String[] args) {
//		1번 
		for (int i = 0; i < 10; i++) {
			System.out.print("*");
		}
		System.out.println("");
		
//		2번
		for (int i = 0; i < 5; i++) {
			System.out.print("커피*");
		}
		
//		3번
		for (int i = 0; i <3; i++) {
			System.out.println("커피*우유");
		}
		
//		4번
		for (int i = 0; i < 3; i++) {
			System.out.println(i + ": 짱");
		}
	}

}
