package 화면db연결;

public class ExamMemberVO {
	private String ID;
	private String PW;
	private String Name;
	private String Tel;
	private String RRN;
	

	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		this.ID = iD;
	}
	public String getPW() {
		return PW;
	}
	public void setPW(String pW) {
		this.PW = pW;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		this.Name = name;
	}
	public String getTel() {
		return Tel;
	}
	public void setTel(String tel) {
		this.Tel = tel;
	}
	public String getRRN() {
		return RRN;
	}
	public void setRRN(String rRN) {
		this.RRN = rRN;
	}
}
